data = {"name": "Guilherme", "age": 28, "phone_number": "3333-1234"}

print(data["name"])  # "Guilherme"
print(data["age"])  # 28
print(data["phone_number"])  # "3333-1234"

data["name"] = "Maria"
data["age"] = 18
data["phone_number"] = "9988-1781"

print(data)  # {"name": "Maria", "age": 18, "phone_number": "9988-1781"}